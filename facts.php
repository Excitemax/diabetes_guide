        <h2>3. Faktor Genetik Berpengaruh<h2>
        <p>Selain gaya hidup, faktor genetik juga berperan dalam risiko mengembangkan diabetes. Jika ada riwayat diabetes dalam keluarga, seseorang memiliki kemungkinan lebih tinggi untuk terkena penyakit ini.

Namun, hal ini tidak berarti bahwa diabetes adalah nasib yang tak terhindarkan. Dengan gaya hidup sehat dan perhatian terhadap faktor risiko, seseorang dapat mengurangi risiko mengembangkan diabetes.<p>
      </section>
      <section>  
        <h2>4. Memengaruhi Berbagai Organ Tubuh<h2>
        <p>Selain pengaruhnya terhadap kadar gula darah, diabetes juga dapat memengaruhi berbagai organ tubuh lainnya. Penyakit ini bisa menyebabkan kerusakan pada mata (retinopati diabetik), ginjal (nefropati diabetik), saraf (neuropati diabetik), dan jantung (penyakit jantung koroner). Karenanya, penting bagi penderita untuk menjaga pengelolaan yang baik dan rutin mengunjungi dokter untuk memantau kondisi mereka.<p>
      </section>

      <section>
        <h2>5. Pencegahan dengan Perubahan Gaya Hidup<h2>
        <p>Meskipun diabetes tergolong penyakit kronis, banyak kasus diabetes tipe 2 yang dapat dicegah dengan perubahan gaya hidup yang sehat. Mengadopsi pola makan seimbang, rutin berolahraga, menjaga berat badan yang sehat, dan menghindari kebiasaan merokok dan konsumsi alkohol berlebihan dapat mengurangi risikonya.<p>
      </section>

